-- ACCOUNTS
CREATE TABLE ACCOUNTS (
    ACCOUNT_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID VARCHAR2(50) NOT NULL,
    FULL_NAME VARCHAR2(200) NOT NULL,
    EMAIL VARCHAR2(200) NOT NULL,
    PASSWORD_HASH VARCHAR2(500) NOT NULL,
    PHONE_NUMBER VARCHAR2(20),
    BALANCE NUMBER(18,2) DEFAULT 0 NOT NULL,
    CURRENCY VARCHAR2(10) DEFAULT 'USD' NOT NULL,
    ACCOUNT_STATUS VARCHAR2(20) DEFAULT 'Active' NOT NULL,
    ACCOUNT_NUMBER VARCHAR2(30) NOT NULL,
    REFRESH_TOKEN VARCHAR2(500),
    REFRESH_TOKEN_EXPIRY TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX UQ_ACCOUNTS_EMAIL ON ACCOUNTS (EMAIL);
CREATE UNIQUE INDEX UQ_ACCOUNTS_ACCOUNT_NUMBER ON ACCOUNTS (ACCOUNT_NUMBER);
    
-- TRANSACTIONS    
CREATE TABLE TRANSACTIONS (
    TRANSACTION_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRANSACTION_REFERENCE VARCHAR2(50) NOT NULL,
    SENDER_ACCOUNT_ID NUMBER(19) NOT NULL,
    RECEIVER_ACCOUNT_ID NUMBER(19) NOT NULL,
    AMOUNT NUMBER(18,2) NOT NULL,
    CURRENCY VARCHAR2(10),
    TRANSACTION_TYPE VARCHAR2(30),
    STATUS VARCHAR2(20),
    DESCRIPTION VARCHAR2(500),
    CATEGORY VARCHAR2(100),
    FEE NUMBER(18,2) DEFAULT 0,
    BALANCE_AFTER NUMBER(18,2),
    TRANSACTION_DATE TIMESTAMP DEFAULT SYSTIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
); 

CREATE UNIQUE INDEX UQ_TXN_REFERENCE ON TRANSACTIONS (TRANSACTION_REFERENCE);
CREATE INDEX IDX_TXN_SENDER ON TRANSACTIONS (SENDER_ACCOUNT_ID);
CREATE INDEX IDX_TXN_RECEIVER ON TRANSACTIONS (RECEIVER_ACCOUNT_ID);

-- BENEFICIARIES
CREATE TABLE BENEFICIARIES (
    BENEFICIARY_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACCOUNT_ID NUMBER(19) NOT NULL,
    NICK_NAME VARCHAR2(100),
    BENEFICIARY_ACCOUNT_NUMBER VARCHAR2(50) NOT NULL,
    BENEFICIARY_NAME VARCHAR2(200) NOT NULL,
    BENEFICIARY_EMAIL VARCHAR2(200),
    BENEFICIARY_PHONE VARCHAR2(20),
    BANK_NAME VARCHAR2(100),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_BEN_ACCOUNT ON BENEFICIARIES (ACCOUNT_ID);

-- NOTIFICATIONS
CREATE TABLE NOTIFICATIONS (
    NOTIFICATION_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACCOUNT_ID NUMBER(19) NOT NULL,
    TITLE VARCHAR2(200)  NOT NULL,
    MESSAGE VARCHAR2(2000) NOT NULL,
    NOTIFICATION_TYPE VARCHAR2(30),
    IS_READ NUMBER(1) DEFAULT 0 NOT NULL,
    REFERENCE_ID VARCHAR2(100),
    REFERENCE_TYPE VARCHAR2(50),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    READ_AT TIMESTAMP
);

CREATE INDEX IDX_NOTIF_ACCOUNT ON NOTIFICATIONS (ACCOUNT_ID, IS_READ);

-- BANK CARDS
CREATE TABLE BANK_CARDS (
    CARD_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACCOUNT_ID NUMBER(19) NOT NULL,
    CARD_HOLDER_NAME VARCHAR2(200) NOT NULL,
    CARD_NUMBER_MASKED VARCHAR2(20),
    CARD_NUMBER_HASH VARCHAR2(500),
    CARD_TYPE VARCHAR2(20),
    CARD_CATEGORY VARCHAR2(20),
    EXPIRY_MONTH VARCHAR2(2),
    EXPIRY_YEAR VARCHAR2(4),
    BANK_NAME VARCHAR2(100),
    IS_DEFAULT NUMBER(1) DEFAULT 0,
    IS_ACTIVE NUMBER(1) DEFAULT 1,
    LINKED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_CARD_ACCOUNT ON BANK_CARDS (ACCOUNT_ID);


